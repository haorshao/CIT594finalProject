package database;

public class Pixel {
		int red;
		int green;
		int blue;
		
		public Pixel(int r, int g, int b) {
			red = r;
			green = g;
			blue = b;
		}
		
		public int getRed() {
			return red;
		}
		
		public int getGreen() {
			return green;
		}
		
		public int getBlue() {
			return blue;
		}
		
	
}
